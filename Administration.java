package Hôpital;

public class Administration {

    private String nom;
    private String prenom;
    private String numeroTel;
    private String login;
    private String motDePasse;

    public Administration(String nom, String prenom, String numeroTel, String login, String motDePasse) {
        this.nom = nom;
        this.prenom = prenom;
        this.numeroTel = numeroTel;
        this.login = login;
        this.motDePasse = motDePasse;
    }
}

